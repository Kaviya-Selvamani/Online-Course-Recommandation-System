export function calcScore(s) {
  return Math.round(
    s.interest * 0.4 +
    s.skill * 0.25 +
    s.rating * 0.15 +
    s.popularity * 0.1 +
    s.career * 0.1,
  );
}

export function getMatch(p) {
  if (p >= 90) return { label: "Perfect Fit", cls: "mp" };
  if (p >= 70) return { label: "Strong Match", cls: "ms" };
  if (p >= 50) return { label: "Growth Zone", cls: "mg" };
  return { label: "Skill Stretch", cls: "mx" };
}

export function getBarColor(p) {
  return p >= 90 ? "#22C55E" : p >= 70 ? "#38BDF8" : p >= 50 ? "#f59e0b" : "#ef4444";
}
